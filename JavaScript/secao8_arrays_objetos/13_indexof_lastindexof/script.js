let nums = [5, 6, 2, 4, 9, 6, 2];

// O método indexOf() retorna o primeiro índice em que o elemento especificado é encontrado no array.
console.log(nums.indexOf(2)); // Saída: 2

// O método lastIndexOf() retorna o último índice em que o elemento especificado é encontrado no array.
console.log(nums.lastIndexOf(2)); // Saída: 6
