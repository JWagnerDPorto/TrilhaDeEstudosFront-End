// Criação de um array chamado 'nomes' com três elementos.
let nomes = ["Wagner", "José", "Maria"];
console.log(nomes); // Exibe o array original.

// Remoção do último elemento do array usando o método pop().
nomes.pop();
console.log(nomes); // Exibe o array após a remoção do último elemento.

// Adição do nome "Pedro" ao final do array usando o método push().
nomes.push("Pedro");
console.log(nomes); // Exibe o array após a adição do novo elemento "Pedro".

