// Criação de um array chamado 'nomes' com quatro elementos.
let nomes = ["Wagner", "José", "Maria", "João"];
console.log(nomes); // Exibe o array original.

// Remoção do primeiro elemento do array usando o método shift().
nomes.shift();
console.log(nomes); // Exibe o array após a remoção do primeiro elemento.

// Adição do nome "Pedro" ao início do array usando o método unshift().
nomes.unshift("Pedro");
console.log(nomes); // Exibe o array após a adição do novo elemento "Pedro".
