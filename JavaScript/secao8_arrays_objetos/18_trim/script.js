
let nome = "\n     Wagner \n";

// A variável "nome" contém uma string com espaços e quebras de linha (\n) antes e depois do nome "Wagner"
let nomeCorrigido = nome.trim();

// Imprime a string original, incluindo espaços e quebras de linha
console.log(nome);

// Imprime a string "nomeCorrigido" após a aplicação do método trim, removendo espaços e quebras de linha
console.log(nomeCorrigido);
