// Criação de um array chamado 'nomes' com três elementos.
let nomes = ["João", "Maria", "José"];

// Início de um loop que percorre o array 'nomes'.
// O loop é executado enquanto 'i' for menor que o comprimento do array.
for (let i = 0; i < nomes.length; i++) {
    // Imprime cada nome do array usando o índice 'i'.
    console.log(nomes[i]);
}
